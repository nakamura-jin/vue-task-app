export default {
  namespaced: true,

  /**********************************************
   * state
   **********************************************/
  state: {
    teams: [],
  },

  /**********************************************
   * getters
   **********************************************/
  getters: {
    teams: (state) => state.teams,
  },

  /**********************************************
   * mutations
   **********************************************/
  mutations: {
    TEAMS(state, teams) {
      state.teams = teams;
    },
  },

  /**********************************************
   * actions
   **********************************************/
  actions: {
    setTeams({ commit }, teams) {
      commit('TEAMS', teams);
    },
  },
};
